#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*d = (unsigned char *)dst;
	const unsigned	char *s = (const unsigned char *)src;
	size_t i;

	if (!dst && !src)
		return (NULL);

	if (d > s)
	{
		i = len;
		while (i-- > 0)
			d[i] = s[i];
	}
	else
	{
		i = 0;
		while (i < len)
		{
			d[i] = s[i];
			i++;
		}
	}
	return (dst);
}
